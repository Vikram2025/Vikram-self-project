
public class Araay2DForEach {

	public static void main(String[] args) {
		int [][] ar= {{10,30,40},{20,60},{22,45,67,89}};
		for(int  ar1[]:ar) {
			System.out.println("the marks of student  are as follows");
		for(int elem:ar1) {
			System.out.print(elem + " ");
			
		}
		System.out.println();
		}

	}

}
